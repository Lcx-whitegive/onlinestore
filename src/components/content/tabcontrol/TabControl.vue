<template>
  <div class="tab-control">
      <div v-for="(item, index) in titles" :key="item" 
      class="tab-control-item" 
      :class="{active: currentIndex == index}"
      @click="change(index)"
      >
          <span>{{item}}</span>
      </div>
  </div>
</template>

<script>
  export default {
    name: 'TabControl',
    data() {
        return {
            currentIndex: 0
        }
    },
    props: {
        titles: {
            type: Array,
            default() {
                return []
            }
        }
    },
    methods: {
        change(index) {
            this.currentIndex = index
            this.$emit('changeTab', index)
        }
    }
  }
</script>

<style scoped>
  .tab-control {
      display: flex;
      height: 40px;
      text-align: center;
      font-size: 15px;
      line-height: 40px;
      background-color: #fff;
  }
  .tab-control-item {
      flex: 1;
  }
  .tab-control-item span {
      padding: 5px;
  }
  .active {
      color: var(--color-high-text);
  }
  .active span {
      border-bottom: 3px solid var(--color-high-text);
  }
</style>